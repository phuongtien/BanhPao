    private void init() {
        main = this;
        titleBar.initJFram(this);
        menu.addEvent(new EventMenuSelected() {
            @Override
            public void menuSelected(int index, int indexSubMenu) {
                if (index == 0 && indexSubMenu == 0) {

                    showForm(new Form_Dashboard());
                }
                if (index == 1 && indexSubMenu == 1) {
                    showForm(new Form_Menu_Man());
                }
                if (index == 2 && indexSubMenu == 1) {
                    showForm(new Form_Emloy());
                }
                if (index == 2 && indexSubMenu == 2) {
                    showForm(new Form_CaLam());
                }
                if (index == 3 && indexSubMenu == 1) {
                    showForm(new Form_HoaDon());
                }
                if (index == 3 && indexSubMenu == 2) {
                    showForm(new Form_CTHD());
                }
                if (index == 4 && indexSubMenu == 0) {
                    showForm(new Form_TroGiup());
                }
                if (index == 5 && indexSubMenu == 0) {
                    showForm(new Form_GioiThieu());
                }
                if (index == 6 && indexSubMenu == 0) {
                    boolean i = MsgBox.confirm(main, "Bạn có muốn đăng xuất?");
                    if (i){
                    Login lg = null;
                    lg = new Login();
                    lg.show();
                    dispose();
                    }
                }
            }
        });
        menu.setSelectedIndex(0, 0);
    }

    public void showForm(Component com) {
        body.removeAll();
        body.add(com);
        body.repaint();
        body.revalidate();
    }

